    private void buttonState(boolean b){
        them.setEnabled(b);
        sua.setEnabled(b);
        capNhat.setEnabled(!b);
        boQua.setEnabled(!b);
    }
    private void updateContentState(){
        if(dangThem || dangSua){
            hoTen.setEnabled(true);
            diaChi.setEnabled(true);
            sdt.setEnabled(true);
        }
        else{
            hoTen.setText("");
            hoTen.setEnabled(false);
            diaChi.setText("");
            diaChi.setEnabled(false);
            sdt.setText("");
            sdt.setEnabled(false);
        }
    }
