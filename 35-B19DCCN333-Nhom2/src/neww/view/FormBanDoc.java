    private void capNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_capNhatActionPerformed
        try{
            String ten, dc, sdts, reg = "\\d+";
            ten = hoTen.getText();
            dc = diaChi.getText();
            sdts = sdt.getText();
            int type = 0;
            if(sdts.isEmpty())
                type = 3;
            if(dc.isEmpty())
                type = 2;
            if(ten.isEmpty())
                type = 1;
            if(type != 0)
                throw new TrongEx(type);
            if(!sdts.matches(reg))
                throw new SDTEx();
            BanDoc bd = new BanDoc(Integer.parseInt(maBanDoc.getText()), ten, dc, sdts);
            if(dangThem){
                tm.addRow(bd.toObject());
                bdlist.add(bd);
            }
            else{
                int row = jTable1.getSelectedRow();
                tm.removeRow(row);
                tm.insertRow(row, bd.toObject());
                bdlist.set(row, bd);
            }
            buttonState(true);
            dangThem = false;
            dangSua = false;
            updateContentState();
        }catch(TrongEx e){
            JOptionPane.showMessageDialog(this, "Không được để trống nhé!");
            if(e.type == 1)
                hoTen.requestFocus();
            else if(e.type == 2)
                diaChi.requestFocus();
            else
                sdt.requestFocus();
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Phải ghi số!");
            sdt.requestFocus();
        }catch(SDTEx e){
            JOptionPane.showMessageDialog(this, "Phải ghi đúng sđt!");
            sdt.requestFocus();
        }
    }//GEN-LAST:event_capNhatActionPerformed

