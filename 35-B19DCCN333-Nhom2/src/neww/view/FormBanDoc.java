    public void loadData(){
        try{
            File f = new File(fbanDoc);
            if(f.exists()){
                bdlist = IOFile.readF("src/controller/BANDOC.DAT");
            }
            else
                f.createNewFile();
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Lỗi file BANDOC.DAT!");
        }
        if(bdlist == null)
            bdlist = new ArrayList<>();
    }
    
