    public void loadData(){
        try{
            File f = new File(fphong);
            if(f.exists()){
                plist = IOFile.readF("src/controller/PH.DAT");
            }
            else
                f.createNewFile();
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Lỗi file PH.DAT!");
        }
        if(plist == null)
            plist = new ArrayList<>();
    }
   
