    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if(row < 0 || row > jTable1.getRowCount()-1){
            JOptionPane.showMessageDialog(this, "Chọn hàng");
        }
        else{
            maPhong.setText(tm.getValueAt(row, 0).toString());
            tenPhong.setText(tm.getValueAt(row, 1).toString());
            for(int i=0; i<phongHop.getItemCount(); i++){
                if(tm.getValueAt(row, 2).toString() == phongHop.getItemAt(i)){
                    phongHop.setSelectedIndex(i);
                    break;
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

