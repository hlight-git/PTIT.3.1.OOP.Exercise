    private void capNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_capNhatActionPerformed
        try{
            String ten;
            ten = tenPhong.getText();
            int type = 0;
            if(ten.isEmpty())
                type = 1;
            if(type != 0)
                throw new TrongEx(type);
            Phong s = new Phong(Integer.parseInt(maPhong.getText()), ten, phongHop.getSelectedItem().toString());
            if(dangThem){
                tm.addRow(s.toObject());
                plist.add(s);
            }
            else{
                int row = jTable1.getSelectedRow();
                tm.removeRow(row);
                tm.insertRow(row, s.toObject());
                plist.set(row, s);
            }
            buttonState(true);
            dangThem = false;
            dangSua = false;
            updateContentState();
        }catch(TrongEx e){
            JOptionPane.showMessageDialog(this, "Không được để trống nhé!");
            if(e.type == 1)
                tenPhong.requestFocus();
        }
    }//GEN-LAST:event_capNhatActionPerformed

